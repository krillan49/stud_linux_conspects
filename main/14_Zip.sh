# zip - команда для одновременной архивации и сжатия(Изначальная папка остается). Создает стандартный сжатый архив универсального формата .zip, подходящего для разархивации как на Linux, так и на Windows системах, тоесть лучше использовать его, если предполагается работа с этим архивом на разных системах. Но при этом сжатие не самое эффективное.

# Сжимает текст 10Kбайт в 320байт.

# $ zip -r archive.zip test               - архивируем и сжимаем папку test с ее содержимвм в архив archive.zip
# $ unzip archive.zip                     - разархивируем archive.zip. Архив остается

# -r  - обязательный флаг. Реверс с конца на начало(только для директорий рекурсивно) ??

