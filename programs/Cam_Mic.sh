# Cheese - самое популярное и простое приложение для камеры. Вы можете делать снимки и записывать видео
# $ sudo apt install cheese            - если оно не установлено
# $ cheese                             - запуск из терминала (Так же можно запустить из Dash)

# GNOME Camera (Webcamoid) - еще одно простое приложение для просмотра и управления камерой
# $ sudo apt install gnome-camera            - если оно не установлено


# 2. Использование терминала:

# v4l2-ctl - позволяет взаимодействовать с устройствами Video4Linux2 (v4l2), к которым относятся веб-камеры
# $ sudo apt install v4l-utils                         - если оно не установлено
# $ v4l2-ctl --list-devices                            - увидеть информацию о камере, покажет список всех устройств v4l2. Обычно веб-камера имеет имя вроде `/dev/video0` или `/dev/video1`
# $ v4l2-ctl --device /dev/video0 --list-formats-ext   - увидеть поддерживаемые форматы и разрешения. Замените /dev/video0 на имя вашего устройства

# ffplay - мультимедийный плеер из пакета ffmpeg может отображать видеопоток с камеры
# $ sudo apt install ffmpeg            - если оно не установлено
# $ ffplay /dev/video0                 - запуск. Замените /dev/video0 на имя вашего устройства. `q` для выхода
# $ ffplay -f v4l2 /dev/video0         - запуск с указанием формата, если просто запуск не работает


# 3. Если камера не определяется:
# - Убедитесь, что камера физически подключена (если это внешняя камера).
# - Проверьте, не отключена ли камера в настройках BIOS/UEFI.
# - Попробуйте перезагрузить компьютер.
# - Убедитесь, что установлены необходимые драйверы (хотя Ubuntu обычно определяет их автоматически). Если возникают проблемы, попробуйте поискать драйверы для вашей конкретной модели камеры.
# - Возможно, другой процесс использует камеру. Попробуйте закрыть все приложения, использующие камеру, и повторите попытку.



echo "                              Работа с микрофоном на Ubuntu"

# 1. Использование встроенных приложений:

# Sound Recorder (или Recorder) - в Ubuntu по умолчанию установлено приложение для записи звука. Найдите его в Dash и запустите
# Настройки звука: Откройте "Настройки" -> "Звук". Перейдите на вкладку "Ввод". Выберите ваш микрофон. Говорите в микрофон.  Если микрофон работает, вы увидите, как индикатор уровня громкости реагирует на ваш голос.


# 2. Использование терминала:

# arecord - утилита командной строки предназначена для записи звука. Она входит в пакет `alsa-utils`
# $ sudo apt install alsa-utils            - установить пакет alsa-utils
# $ arecord -l                             - просмотреть список звуковых устройств. Найдите свою звуковую карту и микрофон. Они будут иметь формат `card X: Device Y` (где X и Y - числа).
# $ arecord -D hw:X,Y -d 5 test.wav        - чтобы записать звук замените X и Y на соответствующие значения, а 5 - на длительность записи в секундах. Затем воспроизведите файл `test.wav` с помощью плеера или командой `aplay test.wav`.

# pavucontrol (PulseAudio Volume Control) - графический интерфейс для управления звуком PulseAudio
# $ sudo apt install pavucontrol           - установить
# Запустите `pavucontrol`. Перейдите на вкладку "Ввод". Выберите свой микрофон. Говорите в микрофон. Вы должны увидеть, как индикатор уровня громкости реагирует на ваш голос.


# 3. Если микрофон не работает:
# - Убедитесь, что микрофон не выключен в настройках звука.
# - Убедитесь, что выбран правильный микрофон в настройках звука.
# - Проверьте, не выключен ли микрофон на самом устройстве (например, кнопка на гарнитуре).
# - Убедитесь, что уровень громкости микрофона не слишком низкий.
# - Возможно, проблема с драйверами. Попробуйте обновить систему или переустановить драйверы ALSA.



